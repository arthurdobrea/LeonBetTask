package model.match;

class Specifiers {
    private String total;
    private String hcp;
    private String cornernr;

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getHcp() {
        return hcp;
    }

    public void setHcp(String hcp) {
        this.hcp = hcp;
    }

    public String getCornernr() {
        return cornernr;
    }

    public void setCornernr(String cornernr) {
        this.cornernr = cornernr;
    }
}
